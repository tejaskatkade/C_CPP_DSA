#include <stdio.h>
void main()
{
    int age = 25;     //int  = 4 bytes
    float petprice = 105.65;  // float = 4 bytes
    double gold = 10.3405646;   // double = 8 bytes
    char wing = 'A';            // char = 1 byte
    //void v;   1/0 bytes

    printf("%d\n",age);         //int      = %d
    printf("%f\n",petprice);    // float   = %f
    printf("%lf\n",gold);       // double  = %lf
    printf("%c\n",wing);        // char    = %c
    
    
   
}